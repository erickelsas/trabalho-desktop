    public void searchByRg(){
        try{
           Doctor doctor = doctorController.searchByRg(rgField.getText(), dbController.getDoctorList());
           if(doctor == null){
            JOptionPane.showMessageDialog(null, "Paciente não encontrado!", "Erro!", JOptionPane.ERROR_MESSAGE);
            return;
           }
           nameField.setText(doctor.getName());
           crmNumberField.setText(Integer.toString(doctor.getCrm().getNum()));
           crmStateField.setText(doctor.getCrm().getEstado());
           especField.setText(doctor.getEspec());
        }catch(RgLengthException rle){
            JOptionPane.showMessageDialog(null, rle.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void close(){
        int resp = JOptionPane.showConfirmDialog(
                    null,
                    "Deseja sair?",
                    "Saida",
                    JOptionPane.YES_NO_OPTION
                  );
        if(resp == 0){
            dispose();
        }
    }
    
