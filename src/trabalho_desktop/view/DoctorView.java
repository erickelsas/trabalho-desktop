    private void addDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDoctorActionPerformed
        Doctor doctor = new Doctor();
        try{
            doctor.setId(0);
            doctor.setName(nameField.getText());
            doctor.setRg(rgField.getText());
            doctor.setRole(1);
            doctor.getCrm().setNum(Integer.parseInt(crmNumberField.getText()));
            doctor.getCrm().setEstado(crmStateField.getText());
            doctor.setEspec(especField.getText());
            ArrayList<Doctor> doctorList = dbController.getDoctorList();
            doctorList = doctorController.createDoctor(doctor, doctorList);
            dbController.setDoctorList(doctorList);
            JOptionPane.showMessageDialog(null, "Usuário criado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            clear();
        }catch(RgLengthException rle){
            JOptionPane.showMessageDialog(null, rle.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }catch(CrmLengthException cle){
            JOptionPane.showMessageDialog(null, cle.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }catch(StateException cse){
            JOptionPane.showMessageDialog(null, cse.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);// TODO add your handling code here:
        } catch (CreateException cex) {
            JOptionPane.showMessageDialog(null, cex.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addDoctorActionPerformed

