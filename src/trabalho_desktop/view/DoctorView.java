    private void deleteDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDoctorActionPerformed
    try{
        ArrayList<Doctor> doctorList = dbController.getDoctorList();
        doctorList = doctorController.deleteDoctor(doctorController.searchByRg(rgField.getText(), doctorList), doctorList);
        dbController.setDoctorList(doctorList);
        clear();
        rgField.requestFocus();
        JOptionPane.showMessageDialog(null, "Usuário removido com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
    } catch(RgLengthException rle){
        JOptionPane.showMessageDialog(null, rle.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);
    }   catch (DeleteException dex) {
            JOptionPane.showMessageDialog(null, dex.getError(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }    // TODO add your handling code here:
    }//GEN-LAST:event_deleteDoctorActionPerformed

